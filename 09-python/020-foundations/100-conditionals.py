value_a = 5
value_b = 3

# if
if value_a > value_b:
    print("Value A is greater than Value B")

# if-else
if value_a > value_b:
    print("Value A is greater than Value B")
else:
    print("Value A is not greater than Value B")

# elif
if value_a > value_b:
    print("Value A is greater than Value B")
elif value_a < value_b:
    print("Value A is less than Value B")
else:
    print("Value A is equal to Value B")
